    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        KhachHang kh = new KhachHang();
        String maKH = txt_maKhachHang.getText().trim();
        String ten = txt_tenKhachHang.getText().trim();
        String sdt = txt_sdt.getText().trim();
        String diaChi = txt_diaChi.getText().trim();
        String diemTL = txt_diemTichLuy.getText().trim();
        String loaiKH = cbb_loaiKhachHang.getSelectedItem().toString();
        
        if (maKH.isEmpty() || ten.isEmpty() || sdt.isEmpty() || diaChi.isEmpty() || diemTL.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống.");
        } else {
            int makh = Integer.parseInt(maKH);
            kh.setMaKhachHang(makh);
            kh.setMaLoaiKhachHang(loaiKH);
            kh.setDiaChi(diaChi);
            kh.setSdt(sdt);
            int diemtl = Integer.parseInt(diemTL);
            kh.getDiemTichLuy();
            this.khSer.them(kh);
            JOptionPane.showMessageDialog(this,"Thêm thành công.");
            addHang();
        }
    }//GEN-LAST:event_btn_themActionPerformed

