    public void addHang() {
        dtm =(DefaultTableModel) tbl_qlKhachHang.getModel();
        dtm.setRowCount(0);
        for (KhachHang kh : khSer.getList()) {
            dtm.addRow(new Object[] {
                kh.getMaKhachHang(),
                kh.getMaLoaiKhachHang(),
                kh.getTenKhachHang(),
                kh.getDiaChi(),
                kh.getSdt(),
                kh.getDiemTichLuy()
            });
        }
    }
    public void addLichSu() {
        dtm = (DefaultTableModel) tbl_lichSuGiaoDich.getModel();
        dtm.setRowCount(0);
        for (HoaDon hd : hdSer.getHoaDon()) {
            dtm.addRow(new Object[] {
                hd.getMaHoaDon(),
                hd.getMaNV(),
                hd.getNgayHDBH(),
                hd.getTongTien()
            });
        }
    }
    public void timKiem() {
        dtm = (DefaultTableModel) tbl_qlKhachHang.getModel();
        dtm.setRowCount(0);
        int maKH = Integer.parseInt(txt_maKhachHang.getText());
        String tenKH = txt_tenKhachHang.getText();
        for (KhachHang kh : khSer.getTimKiem(maKH, tenKH)) {
            dtm.addRow(new Object[] {
                kh.getMaKhachHang(),
                kh.getMaLoaiKhachHang(),
                kh.getTenKhachHang(),
                kh.getDiaChi(),
                kh.getSdt(),
                kh.getDiemTichLuy()
            });
        }
    }
