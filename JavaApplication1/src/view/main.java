    Map<String, String> banMap = new HashMap<>();
    NumberFormat vnFormat = NumberFormat.getCurrencyInstance(new Locale("vi", "VN"));

    void fillTableBan() {
        DefaultTableModel model = (DefaultTableModel) tblBan.getModel();
        model.setRowCount(0);
        try {
            List<BanHang> list = bhrp.selectBan();
            for (BanHang bh : list) {
                String trangThai = bh.getTrangThai();
                banMap.put(bh.getTenBan(), bh.getMaBan());
                Object[] row = {
                    bh.getViTri(),
                    bh.getTenBan(),
                    trangThai
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void fillComboBoxLoaiHang() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiHang.getModel();
        model.removeAllElements();
        try {
            List<BanHang> list = bhrp.selectLH();
            for (BanHang bh : list) {
                model.addElement(bh);
            }
            fillTableMenu();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    void fillTableMenu() {
        DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
        model.setRowCount(0);
        try {
            BanHang bh = (BanHang) cboLoaiHang.getSelectedItem();
            if (bh != null) {
                List<BanHang> list = bhrp.selectByLH(bh.getTenLoaiHang());
                for (BanHang item : list) {
                    String giaFomatted = vnFormat.format(item.getGiaSp());
                    Object[] row = {
                        item.getTenHangHoa(),
                        giaFomatted
                    };
                    model.addRow(row);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    void fillTongTien(String maBan) throws SQLException {
        double tongTien = bhrp.fillTongTien(maBan);
        String tongTienFomatted = vnFormat.format(tongTien);
        txtTongTien.setText(tongTienFomatted);
    }

